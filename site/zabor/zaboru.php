                        <p>
                        <span>Профнастил, иначе называемый профлист</span> — это легкий 
                        универсальный строительный материал, представляющий собой 
                        прокат рулонной стали с трапециевидным или волновым профиле
                        придающим листу необходимую жесткость и делающим его 
                        устойчивым к нагрузкам.  Наша компания использует профнастил, 
                        изготовленный на одном из ведущих отечественных предприятий  
                        «Новолипецком металлургическом комбинате», что гарантирует 
                        качество продукции. Качество используемого для производства 
                        материала подтверждается 25-летней гарантией производителя.
                        Посмотреть способы установки можно <a href="#">на этой странице</a>
                        Заборы из профнастила изготовливаются на основе 
                        сертифицированной стали толщиной листа в 0.5 мм марок С-8,
                        С-15 и С-21. Наша компания осуществляет строительство ограждений следующих категорий:
                        </p>
                        <ul class="textContList">
                            <li>
                            оцинкованный профнастил (не имеет специального полимерного защитного слоя, а потому такой вид 
                            заборов из настила защищен от воздействия влаги только цинковым покрытием, что немного снижает 
                            общий срок эксплуатации изделия. Эти ограждения востребованы для строительных площадок и 
                            временных оград для дачных и садовых участков)
                            </li>
                            <li>
                            профлист с односторонним полимерным покрытием (заборы из профнастила такого типа имеют 
                            большую устойчивость к коррозии, благодаря чему существенно увеличиваются сроки эксплуатации 
                            конструкций. С обратной стороны изделие имеет грунтовое покрытие серого и бежевого цвета)
                            </li>
                            <li>
                            профнастил с двусторонним полимерным покрытием (заборы с двусторонним покрытием имеют 
                            максимальную степень коррозионной стойкости, с обеих сторон окрашиваются в одну цветовую гамму, 
                            зеленую (RAL6005) или коричневую (RAL8017) на выбор)
                            </li>
                        </ul>
